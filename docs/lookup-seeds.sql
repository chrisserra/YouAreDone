SELECT
      @cDate AS [Date]
    , c.[In-House]
    , c.facname
    , c.PatientMRN
    , m.first_name
    , m.last_name
    , m.middle_name
    , m.date_of_birth
    , m.sex
    , m.ssn_sin
    , co.[Emergency Contacts]
    , co.[Emergency Contact Numbers]
    , c.[Room]
    , c.[Bed]
    , pl.item_description AS [Language]
    , CONCAT(mp.LastName, ', ', mp.FirstName) AS [Physician]
    , dx.ActiveDiagnoses
    , oa.PharmacyOrderDescriptions
    , c.Allergies
    , c.address1
    , c.address2
    , c.city
    , c.state
    , c.zip

    -- Raw payer IDs from census_item as-of row
    , dc.FourthPayerID AS [fourth_payer_id]
    , dc.FifthPayerID  AS [fifth_payer_id]

    -- Primary
    , pp.PrimaryPayerGroup
    , pp.PrimaryPayer
    , pp.PrimaryPolicy
    , pp.PrimaryGroupNo
    , pp.PrimaryInsuredName
    , pp.PrimarySubscriberName

    -- Secondary
    , pp.SecondaryPayerGroup
    , pp.SecondaryPayer
    , pp.SecondaryPolicy
    , pp.SecondaryGroupNo
    , pp.SecondaryInsuredName
    , pp.SecondarySubscriberName

    -- Third
    , pp.ThirdPayerGroup
    , pp.ThirdPayer
    , pp.ThirdPolicy
    , pp.ThirdGroupNo
    , pp.ThirdInsuredName
    , pp.ThirdSubscriberName

    -- Fourth
    , pp.FourthPayerGroup
    , pp.FourthPayer
    , pp.FourthPolicy
    , pp.FourthGroupNo
    , pp.FourthInsuredName
    , pp.FourthSubscriberName

    -- Fifth
    , pp.FifthPayerGroup
    , pp.FifthPayer
    , pp.FifthPolicy
    , pp.FifthGroupNo
    , pp.FifthInsuredName
    , pp.FifthSubscriberName

    , c.PatientMedicaidNumber
    , c.PatientMedicareNumber

    , cid.[Medicaid #]
    , cid.[Managed Medicaid #]
    , cid.[HMO/Managed Care/Ins Policy #:]
    , cid.[Part D Policy #]
    , cid.[Supplemental Ins Policy #]
    , cid.[Insurance Policy #:]
    , cid.[Secondary Insurance Policy #]
    , cid.[Co-Insurance Policy #:]
    , cid.[Tertiary Insurance Policy #]
    , cid.[* Insurance Policy #:]
    , cid.[Policy #]
    , cid.[Medicare Advantage Policy #]
    , cid.[Supplemental Ins Name]
    , cid.[HMO/Managed Care/Ins Name]
    , cid.[Part D Name:]
    , cid.[Insurance Name:]
    , cid.[Co-Insurance Name:]
    , cid.[Tertiary Insurance Name]
    , cid.[Secondary Insurance Name]
    , cid.[* Insurance Name:]
FROM [PCC_OASIS].[dbo].[mpi] m
LEFT JOIN [PCC_OASIS].[dbo].[common_code] pl
  ON m.primary_lang_id = pl.item_id
 AND pl.item_code = 'lang'
LEFT JOIN XBI_OHP.dw.ResidentInfo r
  ON r.MasterPatientID = m.mpi_id
JOIN #census c
  ON c.clientID = r.clientID
LEFT JOIN #daily_census dc
  ON dc.clientid   = c.clientID
 AND dc.censusdate = c.cdate
LEFT JOIN #phys_asof pa
  ON pa.clientID = r.clientID
 AND pa.cdate = c.cdate
LEFT JOIN [PCC_OASIS].[dbo].view_ods_medical_professional mp
  ON mp.ProviderID = pa.physicianID
LEFT JOIN #dx_asof dx
  ON dx.clientID = r.clientID
 AND dx.cdate = c.cdate
LEFT JOIN #orders_asof oa
  ON oa.clientID = r.clientID
 AND oa.cdate = c.cdate
LEFT JOIN #contacts co
  ON co.clientID = c.clientID
LEFT JOIN #payers_pivot pp
  ON pp.ClientID = r.clientID
LEFT JOIN #client_ids_pivot cid
  ON cid.ClientID = r.clientID
ORDER BY
    c.facname,
    m.last_name,
    m.first_name;

