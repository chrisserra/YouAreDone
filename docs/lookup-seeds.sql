DECLARE @cDate     date = '2026-03-11';
DECLARE @cDateNext date = DATEADD(day, 1, @cDate);

-------------------------------------------------------------------------------
-- 1) Census (only what we need for @cDate)
-------------------------------------------------------------------------------
SELECT
      c.clientID
    , c.cdate
    , c.payerID
    , c.facid
    , CASE WHEN c.ctype = 'I' THEN 'In-House' ELSE 'Bedhold' END AS [In-House]
    , fp.Allergies
    , fp.PatientMRN
    , fp.PatientMedicaidNumber
    , fp.PatientMedicareNumber
INTO #census
FROM XBI_OHP.dw.censusResident c
LEFT JOIN pcc.view_ods_facility_patient fp
  ON fp.PatientID = c.clientID
WHERE c.cdate = @cDate
  AND c.facid IN ('5','22');

-- 2) Driver table: one row per client
SELECT DISTINCT clientID
INTO #clients
FROM #census;

-------------------------------------------------------------------------------
-- 3) Materialize heavy view: daily census filtered to @cDate + clients
--    Replace OUTER APPLY TOP(1) with temp tables:
-------------------------------------------------------------------------------

-- 3a) Stage eligible census item payer rows (as-of @cDate)
SELECT
      ci.ResidentID
    , ci.EffectiveDate
    , ci.PrimaryPayerID
    , ci.SecondPayerID
    , ci.ThirdPayerID
    , ci.FourthPayerID
    , ci.FifthPayerID
INTO #ci_raw
FROM [PCC_OASIS].[dbo].[view_ods_census_item] ci
JOIN #clients x
  ON x.clientID = ci.ResidentID
WHERE ci.PayerInfoFlag = 'Y'
  AND ci.EffectiveDate <= @cDate;

-- 3b) Rank newest EffectiveDate per resident
SELECT
      r.*
    , ROW_NUMBER() OVER
      (
        PARTITION BY r.ResidentID
        ORDER BY r.EffectiveDate DESC
      ) AS rn
INTO #ci_rank
FROM #ci_raw r;

-- 3c) Keep the single as-of row per resident
SELECT
      ResidentID
    , EffectiveDate
    , PrimaryPayerID
    , SecondPayerID
    , ThirdPayerID
    , FourthPayerID
    , FifthPayerID
INTO #ci_asof
FROM #ci_rank
WHERE rn = 1;

-- 3d) Materialize daily census and LEFT JOIN to #ci_asof
SELECT
      dc.clientid
    , dc.censusdate
    , dc.censusID
    , dc.roomdescription
    , dc.beddescription
    , cia.PrimaryPayerID
    , cia.SecondPayerID
    , cia.ThirdPayerID
    , cia.FourthPayerID
    , cia.FifthPayerID
INTO #daily_census
FROM pcc.view_ods_daily_census dc
JOIN #clients x
  ON x.clientID = dc.clientid
LEFT JOIN #ci_asof cia
  ON cia.ResidentID = dc.clientid
WHERE dc.censusdate = @cDate;

-------------------------------------------------------------------------------
-- Add Bed/Room + Facility info to census
-------------------------------------------------------------------------------
ALTER TABLE #census ADD
      [Room]     varchar(255) NULL
    , [Bed]      varchar(255) NULL
    , facname    varchar(255) NULL
    , address1   varchar(255) NULL
    , address2   varchar(255) NULL
    , city       varchar(100) NULL
    , state      varchar(50) NULL
    , zip        varchar(20) NULL;

UPDATE c
SET
    c.[Room]    = dc.roomdescription,
    c.[Bed]     = dc.beddescription,
    c.facname   = f.facname,
    c.address1  = f.address1,
    c.address2  = f.address2,
    c.city      = f.city,
    c.state     = f.state,
    c.zip       = f.zip
FROM #census c
LEFT JOIN #daily_census dc
  ON dc.clientid   = c.clientID
 AND dc.censusdate = c.cdate
LEFT JOIN dbo.facinfo f
  ON f.facid = c.facid;

-------------------------------------------------------------------------------
-- 4) Emergency Contact (gated by #clients)
-------------------------------------------------------------------------------
SELECT
      rc.clientid
    , EmergencyContact       = CONCAT(rc.lastname, ', ', rc.firstname)
    , EmergencyContactNumber = NULLIF(LTRIM(RTRIM(COALESCE(rc.phonecell, rc.phonehome))), '')
INTO #contacts_raw
FROM [DW].[ResidentContacts] rc
JOIN #clients x
  ON x.clientID = rc.clientid
WHERE rc.specialdesignation = 'Emergency Contact # 1';

SELECT DISTINCT
      clientid
    , EmergencyContact
    , EmergencyContactNumber
INTO #contacts_dedup
FROM #contacts_raw;

SELECT
      clientid
    , [Emergency Contacts] =
        STRING_AGG(CAST(NULLIF(LTRIM(RTRIM(EmergencyContact)), '') AS varchar(max)), ' | ')
    , [Emergency Contact Numbers] =
        STRING_AGG(CAST(EmergencyContactNumber AS varchar(max)), ' | ')
INTO #contacts
FROM #contacts_dedup
GROUP BY clientid;

-------------------------------------------------------------------------------
-- 5) Physician as-of @cDate (latest created_date < @cDateNext)
-------------------------------------------------------------------------------
SELECT
      cs.client_id    AS clientID
    , @cDate          AS cdate
    , cs.created_date
    , cs.staff_id     AS physicianID
INTO #phys_raw
FROM [PCC_OASIS].[dbo].client_staff cs
JOIN #clients x
  ON x.clientID = cs.client_id
WHERE cs.professional_relation_id = 4201
  AND cs.created_date < @cDateNext;

SELECT
      pr.*
    , ROW_NUMBER() OVER (PARTITION BY pr.clientID, pr.cdate ORDER BY pr.created_date DESC) AS rn
INTO #phys_rank
FROM #phys_raw pr;

SELECT
      clientID
    , cdate
    , physicianID
INTO #phys_asof
FROM #phys_rank
WHERE rn = 1;

-------------------------------------------------------------------------------
-- 6) Active Diagnosis Codes as-of @cDate from view_ods_patient_diagnosis
-------------------------------------------------------------------------------
SELECT
      d.PatientID      AS clientID
    , @cDate           AS cdate
    , d.DiagnosisCode
INTO #dx_raw
FROM [PCC_OASIS].[dbo].[view_ods_patient_diagnosis] d
JOIN #clients x
  ON x.clientID = d.PatientID
WHERE d.onsetdate <= @cDate
  AND (d.resolveddate IS NULL OR d.resolveddate > @cDate)
  AND d.deleteddate IS NULL
  AND NULLIF(LTRIM(RTRIM(d.DiagnosisCode)), '') IS NOT NULL;

SELECT DISTINCT
      clientID
    , cdate
    , DiagnosisCode
INTO #dx_dedup
FROM #dx_raw;

SELECT
      clientID
    , cdate
    , ActiveDiagnoses =
        STRING_AGG(CAST(DiagnosisCode AS varchar(max)), ', ')
            WITHIN GROUP (ORDER BY DiagnosisCode)
INTO #dx_asof
FROM #dx_dedup
GROUP BY clientID, cdate;

-------------------------------------------------------------------------------
-- 7) Pharmacy orders as-of census date (gated by #census)
-------------------------------------------------------------------------------
SELECT
      cen.clientID
    , cen.cdate
    , b.bed_desc     AS Bed
    , r.room_desc    AS Room
    , o.description  AS OrderDescription
INTO #orders_raw
FROM #census cen
JOIN [PCC_OASIS].[dbo].clients c
  ON c.client_id = cen.clientID
 AND c.deleted = 'N'
JOIN [PCC_OASIS].[dbo].pho_phys_order o
  ON o.client_id = c.client_id
LEFT JOIN [PCC_OASIS].[dbo].census_item ci
  ON c.current_census_id = ci.census_id
LEFT JOIN [PCC_OASIS].[dbo].bed b
  ON ci.fac_id = b.fac_id
 AND ci.bed_id = b.bed_id
LEFT JOIN [PCC_OASIS].[dbo].room r
  ON b.fac_id = r.fac_id
 AND b.room_id = r.room_id
LEFT JOIN [PCC_OASIS].[dbo].pho_order_type ot
  ON o.order_type_id = ot.order_type_id
LEFT JOIN [PCC_OASIS].[dbo].pho_order_category oc
  ON ot.order_category_id = oc.order_category_id
WHERE c.admission_date IS NOT NULL
  AND oc.category_desc = 'pharmacy'
  AND (o.end_date IS NULL OR o.end_date >= cen.cdate);

SELECT
      clientID
    , cdate
    , Bed
    , Room
    , OrderDescription
INTO #orders_dedup
FROM #orders_raw
GROUP BY clientID, cdate, Bed, Room, OrderDescription;

SELECT
      clientID
    , cdate
    , PharmacyOrderDescriptions =
        STRING_AGG(CAST(OrderDescription AS varchar(max)), ' | ')
            WITHIN GROUP (ORDER BY OrderDescription)
INTO #orders_asof
FROM #orders_dedup
GROUP BY clientID, cdate;

