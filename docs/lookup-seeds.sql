UNION ALL
SELECT
      dc.clientid
    , payerrank = 3
    , payerid   = dc.ThirdPayerID
FROM #daily_census dc
WHERE dc.ThirdPayerID IS NOT NULL
  AND dc.ThirdPayerID > 0

UNION ALL
SELECT
      dc.clientid
    , payerrank = 4
    , payerid   = dc.FourthPayerID
FROM #daily_census dc
WHERE dc.FourthPayerID IS NOT NULL
  AND dc.FourthPayerID > 0

UNION ALL
SELECT
      dc.clientid
    , payerrank = 5
    , payerid   = dc.FifthPayerID
FROM #daily_census dc
WHERE dc.FifthPayerID IS NOT NULL
  AND dc.FifthPayerID > 0;

-------------------------------------------------------------------------------
-- 8b) AR payer "header" as-of @cDate (policy/group/insured)
-------------------------------------------------------------------------------
SELECT
      ap.client_id
    , ap.payer_id
    , ap.policy_no
    , ap.group_no
    , ap.insureds_name
    , ap.effective_date
    , ap.ineffective_date
INTO #ap_raw
FROM [XBI_OHP].[PCC].[ar_client_payer_info] ap
JOIN #ci_payers cip
  ON cip.clientid = ap.client_id
 AND cip.payerid  = ap.payer_id
WHERE ap.effective_date <= @cDate
  AND (ap.ineffective_date IS NULL OR ap.ineffective_date >= @cDate);

SELECT
      r.*
    , ROW_NUMBER() OVER
      (
        PARTITION BY r.client_id, r.payer_id
        ORDER BY r.effective_date DESC
      ) AS rn
INTO #ap_rank
FROM #ap_raw r;

SELECT
      client_id
    , payer_id
    , policy_no
    , group_no
    , insureds_name
    , effective_date
    , ineffective_date
INTO #ap_asof
FROM #ap_rank
WHERE rn = 1;

-------------------------------------------------------------------------------
-- 8c) AR payer "info" as-of @cDate (subscriber contact)
-------------------------------------------------------------------------------
SELECT
      api.client_id
    , api.payer_id
    , api.effective_date
    , api.ineffective_date
    , api.contact_id
INTO #api_raw
FROM [XBI_OHP].[PCC].[ar_client_payer_info] api
JOIN #ci_payers cip
  ON cip.clientid = api.client_id
 AND cip.payerid  = api.payer_id
WHERE api.effective_date <= @cDate
  AND (api.ineffective_date IS NULL OR api.ineffective_date >= @cDate);

SELECT
      r.*
    , ROW_NUMBER() OVER
      (
        PARTITION BY r.client_id, r.payer_id
        ORDER BY r.effective_date DESC
      ) AS rn
INTO #api_rank
FROM #api_raw r;

SELECT
      client_id
    , payer_id
    , contact_id
INTO #api_asof
FROM #api_rank
WHERE rn = 1;

-------------------------------------------------------------------------------
-- 8d) One row per client+rank with computed subscriber fields
-------------------------------------------------------------------------------
SELECT
      cip.clientid AS ClientID
    , cip.payerrank
    , ap.policy_no    AS [Policy]
    , ap.group_no     AS [GroupNo]
    , CASE WHEN ap.insureds_name = -1 THEN 'Self' ELSE ap.insureds_name END AS [InsuredName]
    , CASE
        WHEN api.contact_id IS NULL OR api.contact_id = -1 THEN 'Self'
        ELSE CONCAT(rc.LastName, ', ', rc.FirstName)
      END AS SubscriberName
    , ptg.payerName
    , ptg.payerGroup
INTO #payer_rows
FROM #ci_payers cip
LEFT JOIN #ap_asof ap
  ON ap.client_id = cip.clientid
 AND ap.payer_id  = cip.payerid
LEFT JOIN #api_asof api
  ON api.client_id = cip.clientid
 AND api.payer_id  = cip.payerid
LEFT JOIN [XBI_OHP].DW.ResidentContacts rc
  ON rc.ContactID = api.contact_id
LEFT JOIN dbo.payerToGroup ptg
  ON ptg.pccPayerID = cip.payerid;

-------------------------------------------------------------------------------
-- 8e) Pivot to one row per client (ranks 1-5)
-------------------------------------------------------------------------------
SELECT
      pr.ClientID

    -- Primary (rank 1)
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.[Policy] END)              AS PrimaryPolicy
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.[GroupNo] END)             AS PrimaryGroupNo
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.[InsuredName] END)         AS PrimaryInsuredName
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.SubscriberName END)        AS PrimarySubscriberName
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.[payerGroup] END)          AS PrimaryPayerGroup
    , MAX(CASE WHEN pr.payerrank = 1 THEN pr.[payerName] END)           AS PrimaryPayer

    -- Secondary (rank 2)
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.[Policy] END)              AS SecondaryPolicy
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.[GroupNo] END)             AS SecondaryGroupNo
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.[InsuredName] END)         AS SecondaryInsuredName
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.SubscriberName END)        AS SecondarySubscriberName
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.[payerGroup] END)          AS SecondaryPayerGroup
    , MAX(CASE WHEN pr.payerrank = 2 THEN pr.[payerName] END)           AS SecondaryPayer

    -- Third (rank 3)
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.[Policy] END)              AS ThirdPolicy
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.[GroupNo] END)             AS ThirdGroupNo
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.[InsuredName] END)         AS ThirdInsuredName
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.SubscriberName END)        AS ThirdSubscriberName
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.[payerGroup] END)          AS ThirdPayerGroup
    , MAX(CASE WHEN pr.payerrank = 3 THEN pr.[payerName] END)           AS ThirdPayer

    -- Fourth (rank 4)
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.[Policy] END)              AS FourthPolicy
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.[GroupNo] END)             AS FourthGroupNo
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.[InsuredName] END)         AS FourthInsuredName
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.SubscriberName END)        AS FourthSubscriberName
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.[payerGroup] END)          AS FourthPayerGroup
    , MAX(CASE WHEN pr.payerrank = 4 THEN pr.[payerName] END)           AS FourthPayer

    -- Fifth (rank 5)
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.[Policy] END)              AS FifthPolicy
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.[GroupNo] END)             AS FifthGroupNo
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.[InsuredName] END)         AS FifthInsuredName
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.SubscriberName END)        AS FifthSubscriberName
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.[payerGroup] END)          AS FifthPayerGroup
    , MAX(CASE WHEN pr.payerrank = 5 THEN pr.[payerName] END)           AS FifthPayer
INTO #payers_pivot
FROM #payer_rows pr
GROUP BY pr.ClientID;

-------------------------------------------------------------------------------
-- 9) Client IDs / Policy #s from PCC view_client_ids (pivot by type_description)
-------------------------------------------------------------------------------
SELECT
      vci.client_id
    , vci.type_description
    , vci.[description]
INTO #client_ids_raw
FROM [PCC_OASIS].[dbo].[view_client_ids] vci
JOIN #clients x
  ON x.clientID = vci.client_id
WHERE vci.type_description IN
(
    'HMO/Managed Care/Ins Policy #:',
    'Part D Policy #',
    'Supplemental Ins Policy #',
    'Insurance Policy #:',
    'Secondary Insurance Policy #',
    'Co-Insurance Policy #:',
    'Tertiary Insurance Policy #',
    '* Insurance Policy #:',
    'Policy #',
    'Medicare Advantage Policy #',
    'Medicaid #','Managed Medicaid #','Supplemental Ins Name','HMO/Managed Care/Ins Name','Part D Name:',
    'Insurance Name:','Co-Insurance Name:','Tertiary Insurance Name','Secondary Insurance Name','* Insurance Name:'
);

SELECT
      r.client_id AS ClientID
    , MAX(CASE WHEN r.type_description = 'HMO/Managed Care/Ins Name'      THEN r.[description] END) AS [HMO/Managed Care/Ins Name]
    , MAX(CASE WHEN r.type_description = 'HMO/Managed Care/Ins Policy #:' THEN r.[description] END) AS [HMO/Managed Care/Ins Policy #:]
    , MAX(CASE WHEN r.type_description = 'Part D Name:'                   THEN r.[description] END) AS [Part D Name:]
    , MAX(CASE WHEN r.type_description = 'Part D Policy #'                THEN r.[description] END) AS [Part D Policy #]
    , MAX(CASE WHEN r.type_description = 'Supplemental Ins Policy #'      THEN r.[description] END) AS [Supplemental Ins Policy #]
    , MAX(CASE WHEN r.type_description = 'Supplemental Ins Name'          THEN r.[description] END) AS [Supplemental Ins Name]
    , MAX(CASE WHEN r.type_description = 'Insurance Name:'                THEN r.[description] END) AS [Insurance Name:]
    , MAX(CASE WHEN r.type_description = 'Insurance Policy #:'            THEN r.[description] END) AS [Insurance Policy #:]
    , MAX(CASE WHEN r.type_description = 'Secondary Insurance Name'       THEN r.[description] END) AS [Secondary Insurance Name]
    , MAX(CASE WHEN r.type_description = 'Secondary Insurance Policy #'   THEN r.[description] END) AS [Secondary Insurance Policy #]
    , MAX(CASE WHEN r.type_description = 'Co-Insurance Name:'             THEN r.[description] END) AS [Co-Insurance Name:]
    , MAX(CASE WHEN r.type_description = 'Co-Insurance Policy #:'         THEN r.[description] END) AS [Co-Insurance Policy #:]
    , MAX(CASE WHEN r.type_description = 'Tertiary Insurance Name'        THEN r.[description] END) AS [Tertiary Insurance Name]
    , MAX(CASE WHEN r.type_description = 'Tertiary Insurance Policy #'    THEN r.[description] END) AS [Tertiary Insurance Policy #]
    , MAX(CASE WHEN r.type_description = '* Insurance Name:'              THEN r.[description] END) AS [* Insurance Name:]
    , MAX(CASE WHEN r.type_description = '* Insurance Policy #:'          THEN r.[description] END) AS [* Insurance Policy #:]
    , MAX(CASE WHEN r.type_description = 'Policy #'                       THEN r.[description] END) AS [Policy #]
    , MAX(CASE WHEN r.type_description = 'Medicare Advantage Policy #'    THEN r.[description] END) AS [Medicare Advantage Policy #]
    , MAX(CASE WHEN r.type_description = 'Medicaid #'                     THEN r.[description] END) AS [Medicaid #]
    , MAX(CASE WHEN r.type_description = 'Managed Medicaid #'             THEN r.[description] END) AS [Managed Medicaid #]
INTO #client_ids_pivot
FROM #client_ids_raw r
GROUP BY r.client_id;

